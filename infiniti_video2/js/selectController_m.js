var province;
var city;
var jxs;
var province_name;
var city_name;
var jxs_name;

function initSelect() {
    //var provinceName="山东";
    //var cityName="菏泽";
    $.ajax({
        type: "GET",
        url: "/IP/GetAddress",
        data: {},
        dataType: "json",
        success: function (data) {
            //console.log("ajaxSuccess:", data);
            provinceName = data[1];
            cityName = data[2];
            initDataByServer();
        }
    });

    //initDataByServer();
    function initDataByServer(){
        $("#province option").each(function () {
            var provinceTxt = $(this).val();
            var index = $(this).index();
            if (provinceTxt.indexOf(provinceName) != -1) {
                $('#province')[0].selectedIndex = index;
                province_name = provinceTxt;
                $("#provinceSpan").html(province_name);
                $("#city").attr("disabled", false);
                $("#city").empty();
                $("#city").append("<option value='请选择'>请选择</option>");
                $("#citySpan").html("请选择");
                $("#jxs").empty();
                $("#jsxSpan").html("请选择");
                $("#jxs").attr("disabled", true);
                for (var key in sellerData) {
                    if (key == province_name) {
                        province = sellerData[key];
                        for (var j = 0; j < province.options.length; j++) {
                            $("#city").append("<option value='" + province.options[j].value + "'>" + province.options[j].value + "</option>");
                        }
                    }
                }

                setTimeout(function () {
                    if (province) {
                        var inCity=false;
                        $("#city option").each(function () {
                            var cityTxt = $(this).val();
                            var cityIndex = $(this).index();
                            if (cityTxt.indexOf(cityName) != -1) {
                                inCity=true;
                                $('#city')[0].selectedIndex = cityIndex;
                                city_name = cityTxt;
                                $("#citySpan").html(city_name);
                                for (var i = 0; i < province.options.length; i++) {
                                    if (province.options[i].value == city_name) {
                                        city = province.options[i];
                                        $("#jxs").empty();
                                        $("#jxs").append("<option value='请选择'>请选择</option>");
                                        $("#jxsSpan").html("请选择");
                                        $("#jxs").attr("disabled", false);
                                        for (var j = 0; j < city.children.length; j++) {
                                            $("#jxs").append("<option value='" + city.children[j].value + "'>" + city.children[j].value + "</option>");
                                        }
                                    }
                                }
                            }
                        });

                        if(!inCity){
                            $("#city").attr("disabled", false);
                        }
                    }
                }, 50);
            }
        });
    }

    $("#phoneInput").change(function () {
        var s = $("#phoneInput").val();
        var reg = new RegExp("^[0-9]*$");
        if (!reg.test(s)) {
            alert("请输入正确手机号");
        } else {
            if (s.length != 11) {
                alert("请输入正确手机号");
            }
        }
    });

    $("#city").attr("disabled", true);
    $("#jxs").attr("disabled", true);

    $("#province").change(function () {
        province = null;
        city = null;
        jxs = null;
        province_name = null;
        city_name = null;
        jxs_name = null;
        var provinceName = $(this).children('option:selected').val();
        $("#provinceSpan").html(provinceName);
        if (provinceName == "请选择")return;
        province_name = provinceName;
        $("#city").attr("disabled", false);
        $("#city").empty();
        $("#city").append("<option value='请选择'>请选择</option>");
        $("#citySpan").html("请选择");
        $("#jxs").empty();
        $("#jxsSpan").html("请选择");
        $("#jxs").attr("disabled", true);

        for (var key in sellerData) {
            if (key == provinceName) {
                province = sellerData[key];
                for (var j = 0; j < province.options.length; j++) {
                    $("#city").append("<option value='" + province.options[j].value + "'>" + province.options[j].value + "</option>");
                }
            }
        }
    });

    $("#city").change(function () {

        var cityName = $(this).children('option:selected').val();

        $("#citySpan").html(cityName);

        if (cityName == "请选择")return;

        city_name = cityName;

        for (var i = 0; i < province.options.length; i++) {

            if (province.options[i].value == cityName) {

                city = province.options[i];

                $("#jxs").empty();

                $("#jxs").append("<option value='请选择'>请选择</option>");

                $("#jxsSpan").html("请选择");

                $("#jxs").attr("disabled", false);

                for (var j = 0; j < city.children.length; j++) {

                    $("#jxs").append("<option value='" + city.children[j].value + "'>" + city.children[j].value + "</option>");
                }
            }
        }
    });

    $("#jxs").change(function () {

        var jxsName = $(this).children('option:selected').val();

        $("#jxsSpan").html(jxsName);

        if (jxsName == "请选择")return;

        jxs_name = jxsName;
    });

    $("#cx").change(function () {

        var cxName = $(this).children('option:selected').val();

        $("#cxSpan").html(cxName);

    });
    $("#xb").change(function () {

        var xbName = $(this).children('option:selected').val();

        $("#xbSpan").html(xbName);

    });
    $("#hcTime").change(function () {

        var hcTimeName = $(this).children('option:selected').val();

        $("#hcTimeSpan").html(hcTimeName);

    });
}